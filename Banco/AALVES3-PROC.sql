CREATE PROCEDURE USP_ALUNO_CONSULTAR_TIPO
	@TIPO INT
AS
	IF(@TIPO = 1)
		BEGIN
			SELECT ALUNO.ID_ALUNO AS ID, ALUNO.NOME, ALUNO.SEXO, CURSO.NOME AS CURSO, PERIODO.NOME AS PERIODO FROM ALUNO
			JOIN CURSO_ALUNO ON
			ALUNO.ID_ALUNO = CURSO_ALUNO.ID_ALUNO
			INNER JOIN CURSO ON
			CURSO_ALUNO.ID_CURSO = CURSO.ID_CURSO
			INNER JOIN PERIODO_ALUNO ON
			ALUNO.ID_ALUNO = PERIODO_ALUNO.ID_ALUNO
			INNER JOIN PERIODO ON
			PERIODO_ALUNO.ID_PERIODO = PERIODO.ID_PERIODO ORDER BY CURSO_ALUNO.CADASTRO DESC, PERIODO_ALUNO.CADASTRO DESC
		END
	ELSE IF(@TIPO = 2)
		BEGIN
			SELECT ID_ALUNO AS ID, NOME, EMAIL, TELEFONE FROM ALUNO
		END
	ELSE IF(@TIPO = 3)
		BEGIN
			SELECT ID_ALUNO AS ID, NOME, RUA, NUM, CEP, CIDADE, ESTADO FROM ALUNO
		END
--EXEC USP_ALUNO_CONSULTAR_TIPO "1"
GO

